const preserveWhitespace = (original, translated) => {
  const leading = original.match(/^\s*/)?.[0] || '';
  const trailing = original.match(/\s*$/)?.[0] || '';
  return `${leading}${translated}${trailing}`;
};

const applyPatterns = (text, patterns) => {
  for (const [pattern, replacement] of patterns) {
    if (pattern.test(text)) {
      return text.replace(pattern, replacement);
    }
  }
  return text;
};

const applyReplacements = (text, replacements) => {
  let result = text;
  for (const [from, to] of replacements) {
    result = result.split(from).join(to);
  }
  return result;
};

const looksTranslatable = (text) => /[A-Za-zÀ-ỹĐđ]/.test(text);

export const translateText = (input, targetLanguage = 'vi') => {
  if (typeof input !== 'string') return input;
  if (!looksTranslatable(input)) return input;

  const trimmed = input.trim();
  if (!trimmed) return input;

  let translated = trimmed;

  if (targetLanguage === 'en') {
    translated = VI_TO_EN_EXACT[trimmed] || trimmed;
    if (translated === trimmed) translated = applyPatterns(trimmed, VI_TO_EN_PATTERNS);
    if (translated === trimmed) translated = applyReplacements(trimmed, VI_TO_EN_REPLACEMENTS);
  } else {
    translated = EN_TO_VI_EXACT[trimmed] || trimmed;
    if (translated === trimmed) translated = applyPatterns(trimmed, EN_TO_VI_PATTERNS);
    if (translated === trimmed) translated = applyReplacements(trimmed, EN_TO_VI_REPLACEMENTS);
  }

  return preserveWhitespace(input, translated);
};

export const isTranslatedDifferently = (input, language) => translateText(input, language) !== input;
